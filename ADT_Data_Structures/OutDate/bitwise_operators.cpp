#include<iostream>
using namespace std;

int main() {

        int a=2,b=4;
        cout<<"a&b = "<<(a&b)<<endl;
        cout<<"a|b = "<<(a|b)<<endl;
        cout<<"~a = "<<(~a)<<endl;
        cout<<"a^b = "<<(a^b)<<endl;

        cout<<"5<<1 = "<<(5<<1)<<endl;
        cout<<"5>>1 = "<<(5>>1)<<endl;

        return (0);

}